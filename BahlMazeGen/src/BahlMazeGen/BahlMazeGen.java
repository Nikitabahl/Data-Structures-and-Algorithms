    /**
     * Creates a random maze in a 2D array.
     * 
     * @see <a href="http://en.wikipedia.org/wiki/Maze_generation_algorithm#Recursive_division_method">Recursive_division_method</a>
     * @return 2D array containing a maze
     */
    private static char[][] makeMaze()
    {
        char level[][] = createBlankLevel();
        
        makeMazeRecursive(level, 1, 1, LEVEL_WIDTH-2, LEVEL_HEIGHT-2); //TODO: may need to change but probably not.
        
        return level;
    }

    /**
     * Creates an empty level of standard level height and width. Level will be
     * blank but bordered with wall characters.
     * 
     * @return  2D array containing a maze
     */
    private static char[][] createBlankLevel()
    {   
        char level[][] = new char[LEVEL_HEIGHT][LEVEL_WIDTH];
        
        //reset level to be entirely blank
        for (int y = 0; y < LEVEL_HEIGHT; y++) 
            for (int x = 0; x < LEVEL_WIDTH; x++)
                level[y][x] = ' ';
    
        //top barrier   
        for (int x = 0; x < LEVEL_WIDTH; x++)
            level[0][x] = ICON_WALL;
    
        //bottom barrier
        for (int x = 0; x < LEVEL_WIDTH; x++)
            level[LEVEL_HEIGHT-1][x] = ICON_WALL;
    
        //left barrier
        for (int y = 0; y < LEVEL_HEIGHT; y++)
            level[y][0] = ICON_WALL;
    
        //left barrier
        for (int y = 0; y < LEVEL_HEIGHT; y++)
            level[y][LEVEL_WIDTH-1] = ICON_WALL;
            
        return level;
    }

    //TODO: complete method.
    private static void makeMazeRecursive(char[][]level, int startX, int startY, int endX, int endY)
    {   
        //find random X and Y coordinates for perpendicular lines
        int randX = randBetween(startX, endX);
        int randY = randBetween(startY, endY);
        
        // if-else condition to stop the recursion when size of box becomes 3X3
        if(((randX - startX > 3 )|| (randY - startY >3 )))
        {            
            //draw horizontal line
            for(int length = startX ; length < endX+1; length++)
                level[randY][length]=ICON_WALL;
            
            //draw vertical line
            for (int breadth = startY ; breadth < endY+1; breadth++)
                level[breadth][randX]=ICON_WALL;
            
            //make holes in the lines
            int holeX = randBetween(startY,randY);
            level[holeX][randX] = ICON_BLANK;   
            
            int holeY = randBetween (startX,randX);
            level [randY][holeY] = ICON_BLANK;
            
            //recursion for 1st, 2nd , 3rd and 4th quadrant respectively.
            makeMazeRecursive(level, startX, startY, randX, randY); 
            makeMazeRecursive(level, randX,startY , endX, randY);
            makeMazeRecursive(level, startX, randY, randX, endY);
            makeMazeRecursive(level, randX, randY, endX, endY);
            
        }
    }
    
    //method to get a random number between two numbers
    public static int randBetween(int l, int u)
    {
        Random rand = new Random ();
        int value = rand.nextInt (u-l+1) + l;
        return value;
    }
    
